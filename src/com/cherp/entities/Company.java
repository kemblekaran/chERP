package com.cherp.entities;

public class Company {

	private int id;
	private String name;
	private String pre_add;
	private String sec_add;
	private int mobile;
	private int phone;
	private String state;
	private String city;
	private int pin_code;
	private String own_name;
	private int pan_no;
	private int op_bal;
	private int status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPre_add() {
		return pre_add;
	}
	public void setPre_add(String pre_add) {
		this.pre_add = pre_add;
	}
	public String getSec_add() {
		return sec_add;
	}
	public void setSec_add(String sec_add) {
		this.sec_add = sec_add;
	}
	public int getMobile() {
		return mobile;
	}
	public void setMobile(int mobile) {
		this.mobile = mobile;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int phone) {
		this.phone = phone;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getPin_code() {
		return pin_code;
	}
	public void setPin_code(int pin_code) {
		this.pin_code = pin_code;
	}
	public String getOwn_name() {
		return own_name;
	}
	public void setOwn_name(String own_name) {
		this.own_name = own_name;
	}
	public int getPan_no() {
		return pan_no;
	}
	public void setPan_no(int pan_no) {
		this.pan_no = pan_no;
	}
	public int getOp_bal() {
		return op_bal;
	}
	public void setOp_bal(int op_bal) {
		this.op_bal = op_bal;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	
}
